var favorite_fruits = ["apple", "banana", "orange"];
for (var i = 0; i < favorite_fruits.length; i++) {
    if (favorite_fruits[i] == "apple")
        console.log("You like apple");
    else if (favorite_fruits[i] == "banana")
        console.log("You like banana");
    else if (favorite_fruits[i] == "cherry")
        console.log("You like cherry");
    else if (favorite_fruits[i] == "orange")
        console.log("You like orange");
    else if (favorite_fruits[i] == "mango")
        console.log("You like mango");
}
;
